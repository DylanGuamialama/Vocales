    public void comprobar(String oracion){
        int espacioenblanco=0;
        for(int i=0; i<oracion.length(); i++){
            if(Character.toString(oracion.charAt(i)).equals(" ")){
                espacioenblanco++;
            }
        }
        JOptionPane.showMessageDialog(rootPane, "Numero de espacios en blanco: "+espacioenblanco);
    }
